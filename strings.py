"""
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
#Aspas simples
print('Luiz Carlos')
print(1, 'Luiz "Carlos"')
#Aspas duplas
print("Luiz Carlos")
print(2, "Luiz 'Carlos'")
# Escape
print("Luiz \"Carlos\"")
#r
print(r"Luiz \"Carlos\"")

""" OUTPUT
    Luiz Carlos
    1 Luiz "Carlos"
    Luiz Carlos
    2 Luiz 'Carlos'
    Luiz "Carlos"
    Luiz \"Carlos\"
"""