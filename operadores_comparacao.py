""" OPERADORES DE COMPARAÇÃO REALCIONAIS)
    OP      Significado
    >       maior
    >=      maior ou igual
    <       menor
    <=      menor ou igual
    ==      igual
    !=      diferente
"""

maior = 2 > 1
maior_ou_igual = 3 >= 6
menor = 1 < 0
menor_ou_igual = 2 <= 2
igual = 'a' == 'x'
diferente = 'c' != 'p'

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)

"""OUTPUT
    True
    False
    False
    True
    False
    True
"""